package com.max.idea;

public class DataType {
    public static void main(String[] args) {
        double price = 1399.99;
        byte temperature = 127;
        System.out.println("Переменная типа с плавающей точкой с наибольшим диапазоном чисел - double. Пример значения: " + price);
        System.out.println("Переменная целочисленного типа с наименьшим диапазоном чисел - byte. Наибольшее возможное значение: " + temperature);
    }
}
